const register = asyncHandler(async (req, res, next) => {
  const { name, email, password, role } = req.body;

  try {
    // Email kontrolÃ¼
    const existingUser = await User.findOne({ email });
    if (existingUser) {
      return next(new ApiError(400, "Bu email adresi zaten kayÄ±tlÄ±"));
    }

    // Åžifreyi hashle
    const hashedPassword = await bcrypt.hash(password, 10);

    // DoÄŸrulama tokenÄ± oluÅŸtur
    const verificationToken = crypto.randomBytes(32).toString("hex");

    // KullanÄ±cÄ± oluÅŸtur
    const user = await User.create({
      name,
      email,
      password: hashedPassword,
      role,
      isVerified: false,
      verificationToken,
    });

    // ðŸ”¹ Responseâ€™u hemen dÃ¶n (mail beklenmeden)
    res.status(201).json(
      new ApiResponse(201, "KayÄ±t baÅŸarÄ±lÄ±. LÃ¼tfen e-postanÄ±zÄ± doÄŸrulayÄ±n.", {
        user: {
          _id: user._id,
          name: user.name,
          email: user.email,
          role: user.role,
          isVerified: user.isVerified,
        },
      })
    );

    // ðŸ”¹ Maili arka planda gÃ¶nder
    sendVerificationEmail(user.email, verificationToken).catch(err => {
      console.error("E-posta gÃ¶nderilemedi:", err);
    });

  } catch (error) {
    next(error);
  }
});

// E-posta gÃ¶nderme fonksiyonu
async function sendVerificationEmail(userEmail, token, subject, text) {
  const transporter = nodemailer.createTransport({
    service: "Gmail",
    auth: {
      user: process.env.EMAIL_USER,
      pass: process.env.EMAIL_PASS,
    },
    tls: { rejectUnauthorized: false }, // BazÄ± Render ortamlarÄ± iÃ§in
    connectionTimeout: 20000, // 20 saniye
  });

  const mailOptions = {
    from: "hospital@example.com",
    to: userEmail,
    subject: subject || "Hesap DoÄŸrulama",
    text:
      text ||
      `HesabÄ±nÄ±zÄ± doÄŸrulamak iÃ§in ÅŸu linke tÄ±klayÄ±n: ${process.env.APP_URL}/api/auth/verify/${token}`,
  };

  await transporter.sendMail(mailOptions);
}


const verifyEmail = asyncHandler(async (req, res, next) => {
  const { token } = req.params;
  const user = await User.findOne({ verificationToken: token });
  if (!user) {
    return next(
      new ApiError(400, "GeÃ§ersiz veya sÃ¼resi dolmuÅŸ doÄŸrulama linki.")
    );
  }
  user.isVerified = true;
  user.verificationToken = undefined;
  await user.save();
  res.json(
    new ApiResponse(
      200,
      "E-posta baÅŸarÄ±yla doÄŸrulandÄ±. ArtÄ±k giriÅŸ yapabilirsiniz."
    )
  );
});
